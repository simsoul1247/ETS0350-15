my_tuple = (1, 2, 3, 4, 2, 5, 2)
count_of_twos = my_tuple.count(2)
print(f"The number 2 appears {count_of_twos} times in the tuple.")

string_tuple = ("apple", "banana", "orange", "apple", "grape")
count_of_apples = string_tuple.count("apple")
print(f"The word 'apple' appears {count_of_apples} times in the tuple.")